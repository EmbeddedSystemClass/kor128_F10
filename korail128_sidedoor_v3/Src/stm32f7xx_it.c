/**
* @brief This function handles USART3 global interrupt.
*/
void USART3_IRQHandler(void)
{
  /* USER CODE BEGIN USART3_IRQn 0 */
	uint32_t tmp_flag = 0, tmp_it_source = 0;
	uint8_t r_dat = 0;
	
	tmp_flag = __HAL_UART_GET_FLAG(&huart3, UART_FLAG_RXNE);
	tmp_it_source = __HAL_UART_GET_IT_SOURCE(&huart3, UART_IT_RXNE);
	r_dat = (uint8_t)huart3.Instance->RDR;
	
	if ((tmp_flag != RESET) && (tmp_it_source != RESET)) 
	{ 
		Enqueue(&rx_Int_queue, r_dat);
	}
	__HAL_UART_CLEAR_PEFLAG(&huart3);
	//return;								//해당구문 주석 처리하면 유지보수 프로그램 끼운채로 전원 껏다키면 동작 잘됨

  /* USER CODE END USART3_IRQn 0 */
  HAL_UART_IRQHandler(&huart3);
  /* USER CODE BEGIN USART3_IRQn 1 */

  /* USER CODE END USART3_IRQn 1 */
}

